	public function gregDecode($msg)
	{
		$clefs =$this->getKeys();

		foreach ($clefs as $k => $clef) {
			$msg = str_replace($k, $clef, $msg);
		}

		return $msg;
	}

    function encode($msg){
        $code= array_flip($this->getKeys($this->getKeys()));

        foreach ($code as $k => $clef) {
			$msg = str_replace($k, $clef, $msg);
		}
        return $msg;
    }

